    public void obtenerDatos()
    {
        String nombreCarta = jTextFieldName.getText().replace(" ", "%20");
        String url = "https://db.ygoprodeck.com/api/v7/cardinfo.php?name=";
        
        //Cliente Http que interactua con la api
        HttpClient client = HttpClient.newHttpClient();
        
        //Peticion que se envia al servidor
        HttpRequest request = HttpRequest.newBuilder().uri(URI.create(url + nombreCarta)).build();
        
        try {
             //Se realiza la solicitud y se obtiene la respuesta 
             HttpResponse<String> response = client.send(request, HttpResponse.BodyHandlers.ofString());
             
             //Si la respues es exitosa se muestra en el textPanel
             if(response.statusCode() == 200){
                 JSONObject obj = new JSONObject(response.body());
                 JSONArray data = obj.getJSONArray("data");
                 JSONObject carta = data.getJSONObject(0);
                 
                 jTextField1.setText(String.valueOf(carta.getInt("id")));
                 jTextFieldType.setText(carta.getString("type"));


                 jTextFieldRace.setText(carta.getString("race"));
                 
                 if(carta.has("attribute"))
                    jTextFieldAttribute.setText(carta.getString("attribute"));
                 if(carta.has("archetype"))
                    jTextFieldArchetype.setText(carta.getString("archetype"));
                 if(carta.has("atk"))
                     jTextFieldAtk.setText(String.valueOf(carta.getInt("atk")));
                 if(carta.has("def"))
                     jTextFieldDef.setText(String.valueOf(carta.getInt("def")));
                 if(carta.has("level"))
                     jTextFieldLevel.setText(String.valueOf(carta.getInt("level")));
                 
                 jTextPane1.setText(carta.getString("desc"));
                 
                 JSONArray images = carta.getJSONArray("card_images");
                 if(images.length()>0)
                 {
                     String urlImagen = images.getJSONObject(0).getString("image_url");
                     
                     try {
                         
                         java.net.URL imegenUrl = new java.net.URL(urlImagen);
                         ImageIcon icon = new ImageIcon(imegenUrl);
                         Image image = icon.getImage().getScaledInstance(250, 350, Image.SCALE_SMOOTH);
                         imagen.setIcon(new ImageIcon(image));
                         imagen.setText("");
                     } catch (Exception e) {
                         
                     }
                 }
             }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
